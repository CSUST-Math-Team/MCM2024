%--------------------------------------------------------------------------
%                           数据的极差归一化变换
%--------------------------------------------------------------------------

% 调用rand函数产生一个10行，4列的随机矩阵，每列服从不同的均匀分布
x = [rand(10,1), 5*rand(10,1), 10*rand(10,1), 500*rand(10,1)]

% 调用rscore函数对x按列进行极差规格化变换，
% 返回变换后矩阵R，以及矩阵x各列的最小值构成的向量xmin，各列的极差构成的向量xrange
[R,xmin,xrange] = rscore(x)