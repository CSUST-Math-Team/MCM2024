%--------------------------------------------------------------------------
%                    调用random函数生成二项分布随机数
%--------------------------------------------------------------------------

% 调用random函数生成1000行1列的随机数向量x，其元素服从二项分布B(10,0.3)
x = random('bino', 10, 0.3, 10000, 1);
[fp, xp] = ecdf(x);    % 计算经验累积概率分布函数值
ecdfhist(fp, xp, 50);    % 绘制频率直方图
xlabel('二项分布（n = 10, p = 0.3）随机数');    % 为X轴加标签
ylabel('f(x)');       % 为Y轴加标签