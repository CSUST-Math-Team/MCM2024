%--------------------------------------------------------------------------
%                   调用mnrnd函数生成多项分布随机数
%--------------------------------------------------------------------------

n = 100;    % 多项分布的参数n
p = [0.2  0.3  0.5];    % 多项分布的参数p
% 调用mnrnd函数生成10组3项分布随机数
r = mnrnd(n, p, 10)

% 调用mnrnd函数生成10000组3项分布随机数
r = mnrnd(n, p, 10000);
hist3(r(:,1:2),[50,50])    % 绘制前两维的频数直方图
xlabel('X_1')       % 为X轴加标签
ylabel('X_2')       % 为Y轴加标签
zlabel('频数')       % 为Z轴加标签