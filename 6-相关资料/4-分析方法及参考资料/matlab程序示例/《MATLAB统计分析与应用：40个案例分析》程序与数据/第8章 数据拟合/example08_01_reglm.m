%--------------------------------------------------------------------------
%             读取原始数据，调用自编函数reglm作一元线性回归
%--------------------------------------------------------------------------

ClimateData = xlsread('examp08_01.xls');    % 从Excel文件读取数据
x = ClimateData(:, 1);    % 提取ClimateData的第1列，即年平均气温数据
y = ClimateData(:, 5);    % 提取ClimateData的第5列，即全年日照时数数据
varname = 'x';    % 变量标签
reglm(y,x,[],varname);    % 调用reglm函数作一元线性回归分析